
# Operadores Aritméticos
a = 10
b = 3
print("Soma:", a + b)
print("Subtração:", a - b)
print("Multiplicação:", a * b)
print("Divisão:", a / b)
print("Divisão inteira:", a // b)
print("Resto da divisão:", a % b)
print("Potência:", a ** b)

# Operadores de Atribuição
x = 5
x += 3
print("x += 3 →", x)
x *= 2
print("x *= 2 →", x)

# Operadores de Comparação
print("a == b:", a == b)
print("a != b:", a != b)
print("a > b:", a > b)
print("a < b:", a < b)
